function myFunction() {
  var x = document.getElementById("myDI");
  if (x.innerHTML === "copy") {
    x.innerHTML = "stop";
  } else {
    x.innerHTML = "copy";
  }

  var y = document.getElementById("myDIV");
  if (y.innerHTML === "You are not copying any trader") {
    y.innerHTML = "<img src='../assets/img/copy.jpg' class='avatar avatar-sm me-3'>" + 'Sharita Joujan';
  } else {
    y.innerHTML = "You are not copying any trader";
  }

    var z = document.getElementById("myDI");
  if (z.innerHTML === "copy") {
    z.style.backgroundColor = "#80dfff";
    z.style.color = "black";
  } else {
    z.style.backgroundColor = "#ffffff";
    z.style.color = "#80dfff";
  }
}

