                                    <p>What is important to you? Perhaps it’s their number of followers, or profitability, risk level, the total amount of funds they manage or their return on investment. You might choose a combination of these – it’s completely up to you based on whatever you think is important.</p>
                                    <p>Decide your investment amount, and how you will share it among different managers. Be balanced and don’t put all your eggs in one basket, so choose how much to allocate to each chosen trader if you have selected more than one person to copy. For further</p>
                                    <p>The copy trading platform will then automatically replicate all the selected trader’s positions in your trading account.</p>
                                    <p>Add more funds if you like how the trader is performing. Or, reduce your exposure to one trader and keep your portfolio diversified by not investing too much in a single trader. You can replace your existing ones at any time, just keep in mind that you’ll need a separate Invest account for each trader you decide to follow.</p>
