<p>If you take the time to identify good traders to copy you aren’t taking any more risk than when trading your own account. That is to say trading is inherently risky, and there is always the possibility that you will lose money. Also note that the past performance of a trader is no guarantee of their future performance, so even if they’ve been profitable for three years running, they could have their first losing week right after you decide to follow them. As always, never invest more than you can afford to lose</p>

<h3>How can I find good traders to copy? </h3>
<p>While it might seem tempting to copy the trader with a 300% annual return, in general these traders are taking on far too much risk and eventually will blow up. Instead look for traders who have at least 1-year trading history and a return somewhere between 10% and 30%. The trader should also be active enough that they are placing a minimum of one trade per week. This ensures that they are trying to grow their trading base rather than just locking your money in a single trade. If you want to spread out your risk you can spread out your copy trading between 2-4 different traders.</p>

