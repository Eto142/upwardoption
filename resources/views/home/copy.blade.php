<h3>Copy Trading Terms</h3>
<p>Let’s remind ourselves about the FX market, which is by some distance the biggest traded global market:</p>

<h4>Diversification</h4>
<p>allows you to hedge trading risks by incorporating different trading strategies and assets in a variety of market conditions. This is a key benefit of copy trading.</p>

<h4>Investor</h4>
<p>is the person who follows other traders to utilise their information or directly copy trades from them.</p>

<h4>Slippage</h4>
<p>the pip difference between the order price and the execution price of a trade execution. Due to market volatility or slow internet connection, the order price could change before it reaches the broker for transaction.</p>

<h4>Technical Analysis</h4>
<p>often means charts which a trader uses to interpret historic price action and behaviour for future direction.</p>

<h4>Drawdown</h4>
<P>is the fall in equity in a trader’s account, normally from a relative peak to a relative trough. It can be expressed in absolute terms or in terms of percentage.</P>

<h4>Mirror Trading</h4>
<P>allows you to copy a trader’s actual strategies.</P>

<h4>Social Trading</h4>
<P>allows you to copy transactions made by one or more investors inside a trading network.</P>

<h4>Equity Line</h4>
<p>is the graphical representation of the signal provider’s account balance.</p>

<h4>
Money management</h4>
<p>is the way to control risk and the most important factor is determining success or failure. How much should we assign to each provider and each strategy?</p>

<h4>Stop Levels</h4>
<P>the price the trader chooses to close out a live trade in order to limit your losses if the market moves against you. Stop loss levels depend on the trading strategy.</P>

<h4>Fundamental Analysis</h4>
<p>is the understanding of all news including economic and political to forecast future price movement.</p>

<h4>Signal Provider</h4>
<p>is the trader who identifies the signals to be followed by the investor or follower/copier.</p>

<h4>Symbols</h4>
<P>currency ticker symbols are used in the forex market to represent the pair that is being traded. A currency, such as the dollar, is never bought or sold in absolute terms, but always in relation to another.</P>

