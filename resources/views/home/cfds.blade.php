                                <h4 class="title title-md">Disadvantages of CFDs</h4>
                                <p>Ironically, leverage is one of the biggest disadvantages of CFDs, just as much as it is one of its major appeals. Leverage boosts your profit potential, but when prices go against you, it can leave a devastating puncture in your trading capital. This is because losses are based on the leverage amount and not on your actual trading capital. CFD trading also comes with associated costs. When you open a CFD trade, you have to pay a spread fee which is the difference between the bid and ask prices of an asset. There are also additional costs in the form of rollover fees or swaps for positions held overnight. This is the cost applied for holding a leveraged position (which is essentially borrowed money) for an extended period of time.</p>
                                <p>Market volatility is another source of risk in CFD trading. Prices of financial assets are prone to random fluctuations and sometimes even choppy price action. There can also be price gaps that can occur during high-impact news releases or market openings after the weekends. This volatility can mean that you may miss your desired prices when entering trade positions, or your losses can be amplified when prices go against you. A final drawback for CFDs is that you do not own the underlying asset you are trading; you are simply speculating on its price changes. If you are trading a stock CFD, it means that you have no real shares, you do not hold any voting rights, and are not entitled to any dividends.</p>
