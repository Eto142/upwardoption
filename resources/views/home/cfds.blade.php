                                <h4 class="title title-md">Hedging with CFDs</h4>
                                <p>Hedging is a risk management strategy that involves opening opposite or offsetting trades designed to practically mute the risk exposure of an open trade in the market. CFDs represent an ideal type of derivative to implement a hedging strategy effectively. To start with, they are low-cost and liquid. But they can also be customised (in terms of size and amount) to meet the specific hedging objectives any investor desires.</p>
                                <p>As an example, if you hold $10,000 worth of shares of Tesla in your portfolio, you could hedge the position by selling an equivalent or part amount of  Tesla Stock CFDs. In that way, if Tesla prices fall, the loss in value in your physical shares portfolio will be offset or cancelled by the profits gained by the CFD trade. You can then close out the CFD trade when the downward retracement comes to an end so as to lock in your profits and to give the value of your physical Tesla shares the chance to rise again.</p>
                                <p>CFD hedges are ideal when a market is moving against you (either due to sentiment or overall fundamental reasons) or when the market has moved so much in your favour that any extra gains are likely to be fractional. On the other hand, CFD hedges can be particularly riskier because of leverage; they are therefore not ideal when the underlying market is very volatile or when a retracement has been overextended.</p>
