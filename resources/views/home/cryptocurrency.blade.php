<h3>
Factors to Watch While Trading Cryptocurrencies</h3>
<p>The cryptocurrency market changes very fast, new cryptocurrencies are born, and others disappear. What draws people to the world of cryptocurrencies is the idea of protecting themselves against the devaluation of their own national currency. There are different factors that push the price of cryptocurrencies up or down. To start with, just like with Forex, the higher the demand, the higher the price will be. Also, the purchase of the coin by traders purchasing on speculation can affect the demand and therefore the price.
Media reports and news headlines discussing the growing currency can have an effect on the price of digital currency and Bitcoin in particular. For example, there have been a variety of negative remarks from national regulators in China over the years. In 2013 China announced that financial institutions should stay away from the famous Bitcoin, this resulted in a major drop of the coin by as much as $300. Their initial concern was that it could interfere with normal currency policy information. In 2014 the Bitcoin price dropped once again as Bitcoin exchanges stopped receiving deposits from Banks, and in 2017 China’s central bank was rumoured to implement regulatory action regarding the digital coin exchanges which would have accounted for most of the Bitcoin trading volume. Bitcoin alone at the time of writing in October 2020, is valued at over $201 Billion, this is partially due to people learning what cryptocurrencies are and considering them as an investment vehicle to add to their investment portfolio.
</p>

