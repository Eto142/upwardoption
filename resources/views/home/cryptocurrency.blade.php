                                    <h3>What is a Cryptocurrency?</h3>
                                    <p>Cryptocurrencies are encrypted decentralised digital currencies that are transferred between individuals in which transactions are verified and records maintained by a decentralized system using cryptography, rather than by a centralized authority.
"decentralized cryptocurrencies such as bitcoin now provide an outlet for personal wealth that is beyond restriction and confiscation. These currencies are not tangible and exist only in an electronic form; it is a digital asset that exists and remains as data. They allow a person to send money just like they would send an email, with much faster transaction times compared to using a bank, plus minimal fees, no credit cards necessary and no middleman adding on charges.</p>
<p>The joint bookkeeping process is called a “Blockchain”. It is public and is distributed across a huge network, and everybody that has Bitcoin has a copy of the ledger and its transactions, which creates a community of trust. Each cryptocurrency is individually identifiable and programmable based on a very complex digital code. While Bitcoin is not the only digital currency on the market, it is indeed the first and most popular one and stands as the digital “gold standard” within the industry. Some of the top-ranking altcoins include Litecoin, Ethereum,Bitcoin cash, Bitcoin Gold, NEO, IOTA, Stellar Lumen and  EOS to name a few.</p>

<h3>How Do Cryptocurrencies Work?</h3>
<p>Primarily, cryptocurrencies rely on blockchain technology to complete a transaction via an intricate P2P network. Once a transfer request is entered into the network, it is validated by the network and added to a pool of other transactions to create a block of data for the ledger, which is then entered into the existing blockchain. Once the block is successfully added to the chain, the transaction is approved and completed.</p>

